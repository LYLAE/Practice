
public class practice_210731_1 {
	
public static void main(String[] args) {
	
	char c = 'c'; // char : 2byte
	System.out.println("c = " + c);
	
	int i = 10; //int : 4byte
	System.out.println("i = " + i);
	
	double d = 10.123; // double : 8byte
	System.out.println("d = " + d);
	
	boolean b = false; // boolean : 1byte
	System.out.println("b = " + b);
	
	String s = "Hello Jave World!!!!!";
	System.out.println("s = " +s);
	
	s = "Good";
	System.out.println("s = " +s);
}
}
